package cn.edu.zucc.model;

public class manjian {
	 private int manjian_id;
	 private float manjian_money;
	 private float youhui_money;
	 private int shifou;
	 
	public int getManjian_id() {
		return manjian_id;
	}
	public void setManjian_id(int manjian_id) {
		this.manjian_id = manjian_id;
	}
	public float getManjian_money() {
		return manjian_money;
	}
	public void setManjian_money(float manjian_money) {
		this.manjian_money = manjian_money;
	}
	public float getYouhui_money() {
		return youhui_money;
	}
	public void setYouhui_money(float youhui_money) {
		this.youhui_money = youhui_money;
	}
	public int getShifou() {
		return shifou;
	}
	public void setShifou(int shifou) {
		this.shifou = shifou;
	}
	 
}
